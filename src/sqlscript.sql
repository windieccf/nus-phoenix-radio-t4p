`USERNAME` VARCHAR(50) NULL DEFAULT NULL COLLATE 'latin1_swedish_ci',
`PASSWORD` VARCHAR(50) NULL DEFAULT NULL COLLATE 'latin1_swedish_ci',
`FIRST_NAME` VARCHAR(50) NULL DEFAULT NULL COLLATE 'latin1_swedish_ci',
`LAST_NAME` VARCHAR(50) NULL DEFAULT NULL COLLATE 'latin1_swedish_ci',
`DOB` DATETIME NULL DEFAULT NULL,
`EMAIL` VARCHAR(50) NULL DEFAULT NULL COLLATE 'latin1_swedish_ci',
`ADDRESS` TEXT NULL COLLATE 'latin1_swedish_ci',
`CONTACT_HOME` VARCHAR(50) NULL DEFAULT NULL COLLATE 'latin1_swedish_ci',
`CONTACT_MOBILE` VARCHAR(50) NULL DEFAULT NULL COLLATE 'latin1_swedish_ci',
`JOIN_DATE` DATETIME NULL DEFAULT NULL,
`STATUS` CHAR(1) NULL DEFAULT NULL COLLATE 'latin1_swedish_ci',
`CREATED_BY_ID` BIGINT(20) NULL DEFAULT NULL,
`MODIFIED_BY_ID` BIGINT(20) NULL DEFAULT NULL,
